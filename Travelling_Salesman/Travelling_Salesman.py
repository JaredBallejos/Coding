#finds the address of the minimum mileage list.  The space complexity is O(N).  The time complexity is O(N).
def milesListToAddress(a, minimum, addressList):
    addresses = []
    for x in addressList:
        addresses.append(x[1])
    b = list(a.items())
    for n, x in b:
        if  x == minimum and n in addresses:
            return n

